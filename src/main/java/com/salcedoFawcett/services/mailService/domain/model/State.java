package com.salcedoFawcett.services.mailService.domain.model;

public class State {
    private int id;
    private String name;

    public State() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
